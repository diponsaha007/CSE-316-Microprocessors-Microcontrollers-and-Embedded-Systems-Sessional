.MODEL SMALL


.STACK 100H


.DATA
	CR EQU 0DH
	LF EQU 0AH
	MSG DB 'Valid Password$'
	MSG2 DB 'Invalid Password$'
	F1 DB 0
	F2 DB 0
	F3 DB 0
.CODE

MAIN PROC
	;DATA SEGMENT INITIALIZATION
    MOV AX, @DATA
    MOV DS, AX
    
INPUT:
	MOV AH, 1
    INT 21H
	;IF(AL>= 41H AND AL <= 5AH) F1 = 1
	CMP AL , 41H
	JL COND2
	CMP AL , 5AH
	JG COND2
	MOV F1,1
COND2:
	;IF(AL>= 61H AND AL <= 7AH) F2 = 1
	CMP AL , 61H
	JL COND3
	CMP AL , 7AH
	JG COND3
	MOV F2,1
COND3:
	;IF(AL>= 30H AND AL <= 39H) F3 = 1
	CMP AL , 30H
	JL COND4
	CMP AL , 39H
	JG COND4
	MOV F3,1
COND4:
	;IF(AL>= 21H AND AL <= 7EH) LOOP ELSE BREAK
	CMP AL , 21H
	JL EXIT_LOOP
	CMP AL , 7EH
	JG EXIT_LOOP
	JMP INPUT
EXIT_LOOP:
	;PRINT NEW LINE
	MOV AH, 2
	MOV DL , LF
	INT 21H
	MOV DL , CR
	INT 21H
	;F1+F2+F3 = 3 FOR VALID PASS
	MOV AL , F1
	ADD AL , F2
	ADD AL , F3
	CMP AL , 3
	JNE INVALID
	LEA DX, MSG
    MOV AH, 9
    INT 21H
	JMP EXIT
INVALID:
	LEA DX, MSG2
    MOV AH, 9
    INT 21H
EXIT:
    ;DOS EXIT
    MOV AH, 4CH
    INT 21H

MAIN ENDP
END MAIN
