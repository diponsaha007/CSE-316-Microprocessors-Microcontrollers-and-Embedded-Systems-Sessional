.MODEL SMALL


.STACK 100H


.DATA
	CR EQU 0DH
	LF EQU 0AH
	MSG1 DB 'ENTER AN UPPERCASE LETTER : $'
	MSG2 DB 'PREVIOUS LETTER IN LOWERCASE FORMAT IS : $'
	MSG3 DB 'ONE S COMPLEMENT IS : $'
	CHAR DB ?
	ANS DB ?

.CODE

MAIN PROC
	;DATA SEGMENT INITIALIZATION
    MOV AX, @DATA
    MOV DS, AX
    
	;INPUT CHAR
	LEA DX, MSG1
    MOV AH, 9
    INT 21H
	
	MOV AH, 1
    INT 21H
    MOV CHAR, AL
	
	;PRINT NEW LINE
	MOV AH, 2
	MOV DL , LF
	INT 21H
	MOV DL , CR
	INT 21H
	
	;PREVIOUS LETTER 
	MOV BH , CHAR
	SUB BH , 1
	ADD BH , 32
	MOV ANS , BH
	
	; PRINT ANS
	LEA DX, MSG2
    MOV AH, 9
    INT 21H
	
	MOV AH, 2
	MOV DL, ANS
	INT 21H
	
	;PRINT NEW LINE
	MOV AH, 2
	MOV DL , LF
	INT 21H
	MOV DL , CR
	INT 21H
    
    ;ONE S COMPLEMENT
	MOV BH , CHAR
	NEG BH
	SUB BH,1
	MOV ANS , BH
	
	; PRINT ANS
	LEA DX, MSG3
    MOV AH, 9
    INT 21H
	
	MOV AH, 2
	MOV DL, ANS
	INT 21H
	
	;PRINT NEW LINE
	MOV AH, 2
	MOV DL , LF
	INT 21H
	MOV DL , CR
	INT 21H
	
	
    ;DOS EXIT
    MOV AH, 4CH
    INT 21H

MAIN ENDP
END MAIN
